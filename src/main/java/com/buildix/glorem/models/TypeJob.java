package com.buildix.glorem.models;

/**
 * @author Antoxas
 *
 */
public class TypeJob {
	private Integer id;
	private String chapterName;
	private String workDescription;
	private String price;
	private String unitName;
	private String minimumCharge;
	private String amount;
	private String total;
	private Integer customerId;
	private String customerAmount;
	private String customerTotal;
	/**
	 * @return the chapterName
	 */
	public String getChapterName() {
		return chapterName;
	}
	/**
	 * @param chapterName the chapterName to set
	 */
	public void setChapterName(String chapterName) {
		this.chapterName = chapterName;
	}
	/**
	 * @return the unitName
	 */
	public String getUnitName() {
		return unitName;
	}
	/**
	 * @param unitName the unitName to set
	 */
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
		
	/**
	 * @return the workDescription
	 */
	public String getWorkDescription() {
		return workDescription;
	}
	/**
	 * @param workDescription the workDescription to set
	 */
	public void setWorkDescription(String workDescription) {
		this.workDescription = workDescription;
	}
	/**
	 * @return the price
	 */
	public String getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(String price) {
		this.price = price;
	}
	
	/**
	 * @return the minimumCharge
	 */
	public String getMinimumCharge() {
		return minimumCharge;
	}
	/**
	 * @param minimumCharge the minimumCharge to set
	 */
	public void setMinimumCharge(String minimumCharge) {
		this.minimumCharge = minimumCharge;
	}
	/**
	 * @return the amount
	 */
	public String getAmount() {
		return amount;
	}
	/**
	 * @param amount the amount to set
	 */
	public void setAmount(String amount) {
		this.amount = amount;
	}
	/**
	 * @return the total
	 */
	public String getTotal() {
		return total;
	}
	/**
	 * @param total the total to set
	 */
	public void setTotal(String total) {
		this.total = total;
	}
	/**
	 * @return the customerId
	 */
	public Integer getCustomerId() {
		return customerId;
	}
	/**
	 * @param customerId the customerId to set
	 */
	public void setCustomerId(Integer customerId) {
		this.customerId = customerId;
	}
	/**
	 * @return the customerAmount
	 */
	public String getCustomerAmount() {
		return customerAmount;
	}
	/**
	 * @param customerAmount the customerAmount to set
	 */
	public void setCustomerAmount(String customerAmount) {
		this.customerAmount = customerAmount;
	}
	/**
	 * @return the customerTotal
	 */
	public String getCustomerTotal() {
		return customerTotal;
	}
	/**
	 * @param customerTotal the customerTotal to set
	 */
	public void setCustomerTotal(String customerTotal) {
		this.customerTotal = customerTotal;
	}

}
